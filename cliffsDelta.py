class Counts():  # Add/delete counts of numbers.
    def __init__(self, inits = []):
      self.zero()
      for number in inits: self +number
    def zero(self): self.n = self.mu = self.m2 = self.sum = 0
    def sd(self)  :
      if self.n < 2: return self.mu
      else:
        return (max(0, self.m2) * 1.0 / (self.n - 1)) ** 0.5
    def __add__(self, x):
      self.n += 1
      self.sum += x
      delta = x - self.mu
      self.mu += delta / (1.0 * self.n)
      self.m2 += delta * (x - self.mu)
    def __repr__(self):
      return str(dict(n = self.n, mu = self.mu, sd = self.sd()))


def cliffsDelta(lst1, lst2,
                dull = [0.147,  # small
                        0.33,  # medium
                        0.474  # large
                        ][0]):
  "Returns true if there are more than 'dull' differences"
  m, n = len(lst1), len(lst2)
  lst2 = sorted(lst2)
  j = more = less = 0
  for repeats, x in runs(sorted(lst1)):
    while j <= (n - 1) and lst2[j] < x:
      j += 1
    more += j * repeats
    while j <= (n - 1) and lst2[j] == x:
      j += 1
    less += (n - j) * repeats
  d = abs((more - less) / (m * n))
  return d, abs(d) > dull

def runs(lst):
  "Iterator, chunks repeated values"
  for j, two in enumerate(lst):
    if j == 0:
      one, i = two, 0
    if one != two:
      yield j - i, one
      i = j
    one = two
  yield j - i + 1, two

